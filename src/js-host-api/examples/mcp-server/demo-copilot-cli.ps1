            Write-Separator

            if ($i + 1 -lt $DemoPrompts.Count) {
                $answer = Read-Host 'Next demo? (Enter to continue, q to quit)'
                if ($answer -eq 'q') {
                    # "I'll be back." — The Terminator (1984)
                    Write-Info 'Thanks for watching! "I''ll be back." — The Terminator (1984)'
                    exit 0
                }
            }
        }

        Write-Host '🎉 All demos complete!' -ForegroundColor Green
        # "That's all, folks!" — Porky Pig (well, 1930s, close enough)
        Write-Host '"That''s all, folks!" — Porky Pig (well, 1930s, close enough)' -ForegroundColor DarkGray
    }
